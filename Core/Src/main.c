int8_t getValue(uint16_t adc_value) {
	if(adc_value < 0) adc_value = 0;
	if(adc_value > 1023) adc_value = 1023;
	//Formula para mapear valor
	//valor_saida = (adc_value - in_min) * ((out_max - out_min) / (in_max - in_min)) + out_min
	//in_min = 0 in_max = 1023 out_min = -127 out_max = 127
	int8_t result = (int8_t) ((adc_value * (254.0 / 1023.0)) - 127);
	return result;
}
